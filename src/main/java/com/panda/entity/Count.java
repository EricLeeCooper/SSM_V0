package com.panda.entity;

public class Count {
	private int id;
	private String userid;
	private int count;
	public Count(int id, String userid, int count) {
		super();
		this.id = id;
		this.userid = userid;
		this.count = count;
	}
	public Count() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	@Override
	public String toString() {
		return "Count [id=" + id + ", userid=" + userid + ", count=" + count
				+ "]";
	}
	
	
}
